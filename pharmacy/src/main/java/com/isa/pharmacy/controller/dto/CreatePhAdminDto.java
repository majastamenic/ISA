package com.isa.pharmacy.controller.dto;

import com.isa.pharmacy.domain.enums.Role;

public class CreatePhAdminDto {
    private String email;
    private String name;
    private String surname;
    private String address;
    private String city;
    private String country;
    private String phone;
    private Boolean active;
    private Role role;
    private Long pharmacyId;

    public CreatePhAdminDto(){}

    public CreatePhAdminDto(String email, String name, String surname, String address, String city, String country, String phone, Boolean active, Role role, Long pharmacyId) {
        this.email = email;
        this.name = name;
        this.surname = surname;
        this.address = address;
        this.city = city;
        this.country = country;
        this.phone = phone;
        this.active = active;
        this.role = role;
        this.pharmacyId = pharmacyId;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public Boolean getActive() {
        return active;
    }

    public void setActive(Boolean active) {
        this.active = active;
    }

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }

    public Long getPharmacyId() {
        return pharmacyId;
    }

    public void setPharmacyId(Long pharmacyId) {
        this.pharmacyId = pharmacyId;
    }
}
